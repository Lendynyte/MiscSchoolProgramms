    /**
     * This method resolves mathematical operations set by previous actions and sets
     * variables back to their initial values
     * @param evt mouse released event
     */
    private void jButton14MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton14MouseReleased
        druheCislo = Double.parseDouble(jTextField1.getText());
        //druheCislo -= prvniCislo * Math.pow(10, Double.toString(prvniCislo).length() - jTextField1.getText().length()); 
        switch (operace) {
            case "+":
                vysledek = prvniCislo + druheCislo;
                prvniCislo = 0;
                druheCislo = 0;
                jeCarka = false;
                jTextField1.setText(Double.toString(vysledek));//mainClass.zkratCislo(vysledek, 12))
                vysledek = 0;
                break;
            case "-":
                vysledek = prvniCislo - druheCislo;
                prvniCislo = 0;
                druheCislo = 0;
                jeCarka = false;
                jTextField1.setText(Double.toString(vysledek));
                vysledek = 0;
                break;
            case "*":
                vysledek = prvniCislo * druheCislo;
                prvniCislo = 0;
                druheCislo = 0;
                jeCarka = false;
                jTextField1.setText(Double.toString(vysledek));
                vysledek = 0;
                break;
            case "/":
                if (druheCislo != 0) {
                    vysledek = prvniCislo / druheCislo;
                    prvniCislo = 0;
                    druheCislo = 0;
                    jeCarka = false;
                    jTextField1.setText(Double.toString(vysledek));
                    vysledek = 0;
                } else {
                    jTextField1.setText("Nelze delit 0 !!!");
                    prvniCislo = 0;
                    druheCislo = 0;
                    jeCarka = false;
                    vysledek = 0;
                }
                break;
            default:
                    //jTextField1.setText("");
                break;
        }
        operace = "";
        jePrvni = true;
    }//GEN-LAST:event_jButton14MouseReleased

